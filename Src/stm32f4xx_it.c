/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
    uint32_t uRecvLen;
    uint16_t result = 0;
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
    /* additional by user for idle */
    if(__HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_IDLE) != RESET) //检测是否发生空闲中断
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart2);//清除中断标志位
        
        result = __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);
        
        // 判断IDLE是在DMA的前部分还是后部分产生的
        if (result <= huart2.RxXferSize/2) {
            uRecvLen = huart2.RxXferSize - dma_buffer.offset - result;
            dma_buffer.write(huart2.pRxBuffPtr + dma_buffer.offset, uRecvLen);
            dma_buffer.offset += uRecvLen;
            //printf("offset1:%d\r\n", dma_buffer.offset);
        } else {
            uRecvLen = huart2.RxXferSize - dma_buffer.offset - result;
            dma_buffer.write(huart2.pRxBuffPtr + dma_buffer.offset, uRecvLen);
            dma_buffer.offset += uRecvLen;
            //printf("offset2:%d\r\n", dma_buffer.offset);
        }
	}

  /* USER CODE END USART2_IRQn 1 */
}

